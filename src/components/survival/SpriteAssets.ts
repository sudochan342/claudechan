// Get animation frames for a specific action
export function getAnimationFrames(action: string): number[][][] {
  switch (action) {
    case 'walk':
    case 'walking':
    case 'move':
      return CHARACTER_WALK;
    case 'chop':
    case 'chopping':
    case 'attack':
    case 'fighting':
      return CHARACTER_CHOP;
    case 'gather':
    case 'gathering':
      return CHARACTER_GATHER;
    default:
      return CHARACTER_IDLE;
  }
}
