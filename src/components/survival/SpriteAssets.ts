export const HERO_IDLE = [HERO_IDLE_1, HERO_IDLE_2];
export const HERO_WALK = [HERO_WALK_1, HERO_WALK_2];
export const HERO_CHOP = [HERO_ATTACK, HERO_IDLE_1]; // Reuse attack for chop
export const HERO_GATHER = [HERO_IDLE_2, HERO_IDLE_1]; // Bending animation

// Get sprite frames for action
export function getActionSprites(action: string): number[][][] {
  const a = action.toLowerCase();
  if (a.includes('walk') || a.includes('move') || a.includes('run')) return HERO_WALK;
  if (a.includes('chop') || a.includes('attack') || a.includes('fight')) return HERO_CHOP;
  if (a.includes('gather') || a.includes('pick') || a.includes('collect')) return HERO_GATHER;
  return HERO_IDLE;
}
